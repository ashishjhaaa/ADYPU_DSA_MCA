#include <bits/stdc++.h>
using namespace std;

/*
Logical Operators in C++

&&  Logical AND
||  Logical OR
!   Logical NOT

*/

int main()
{
    int a = 10, b = 5;

    cout << "Logical Operators " << endl << endl;

    cout << "(a > 5 && b > 2) : " << (a > 5 && b > 2) << endl;
    cout << "(a < 5 || b > 2) : " << (a < 5 || b > 2) << endl;
    cout << "!(a > b)        : " << !(a > b) << endl;
}
