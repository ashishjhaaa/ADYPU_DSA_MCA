#include <bits/stdc++.h>
using namespace std;

/*
Relational Operators in C++

==  Equal to
!=  Not equal to
>   Greater than
<   Less than
>=  Greater than or equal to
<=  Less than or equal to

*/

int main()
{
    int a = 10, b = 20;

    cout << "Relational Operators " << endl;
    cout << "a = " << a << ", b = " << b << endl << endl;

    cout << "a == b : " << (a == b) << endl;
    cout << "a != b : " << (a != b) << endl;
    cout << "a > b  : " << (a > b) << endl;
    cout << "a < b  : " << (a < b) << endl;
    cout << "a >= b : " << (a >= b) << endl;
    cout << "a <= b : " << (a <= b) << endl;
}
